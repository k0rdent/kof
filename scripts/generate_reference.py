# ---------------------------------------------------------------------------


def generate_datasources(docs: list[dict]) -> dict[str, Any]:
    """Generate datasources.yaml from rendered GrafanaDatasource resources."""
    datasources = []

    for doc in get_resources_by_kind(docs, "GrafanaDatasource"):
        meta = doc.get("metadata", {})
        spec = doc.get("spec", {})
        ds_spec = spec.get("datasource", {})

        datasources.append({
            'name': ds_spec.get("name", meta.get("name", "")),
            'type': ds_spec.get("type", ""),
            'uid': ds_spec.get("uid", ""),
            'access': ds_spec.get("access", ""),
            'url': ds_spec.get("url", ""),
            'resource_name': meta.get("name", ""),
            'namespace': meta.get("namespace", ""),
            'is_default': ds_spec.get("isDefault", False),
        })

    # Also check for datasource references in chart templates (not rendered)
    # kof-storage has datasources too
    storage_ds_templates = list(
        (CHARTS_DIR / "kof-storage" / "templates" / "grafana").glob("*datasource*")
    ) if (CHARTS_DIR / "kof-storage" / "templates" / "grafana").exists() else []

    additional_sources = []
    for f in storage_ds_templates:
        text = f.read_text()
        # Extract name from template
        name_match = re.search(r'name:\s*(.+)', text[text.find('datasource:'):] if 'datasource:' in text else text)
        type_match = re.search(r'type:\s*(\w+)', text[text.find('datasource:'):] if 'datasource:' in text else text)
        if name_match:
            additional_sources.append({
                'name': re.sub(r'\{\{.*?\}\}', 'TMPL', name_match.group(1).strip()),
                'type': type_match.group(1).strip() if type_match else '',
                'source_template': str(f.relative_to(REPO_ROOT)),
                'note': 'From kof-storage chart (requires storage enabled)',
            })

    return {
        'total_datasources': len(datasources),
        'rendered_datasources': datasources,
        'additional_chart_datasources': additional_sources,
    }


