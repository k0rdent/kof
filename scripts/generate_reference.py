def generate_collectors(docs: list[dict]) -> dict[str, Any]:
    """Generate collectors.yaml from rendered OpenTelemetryCollector resources."""
    collectors = []

    for doc in get_resources_by_kind(docs, "OpenTelemetryCollector"):
        meta = doc.get("metadata", {})
        spec = doc.get("spec", {})

        # Parse OTel config from spec.config
        config_str = spec.get("config", "")
        otel_config = {}
        if isinstance(config_str, str) and config_str:
            try:
                otel_config = yaml.safe_load(config_str)
            except yaml.YAMLError:
                pass
        elif isinstance(config_str, dict):
            otel_config = config_str

        # Extract pipeline topology
        service = otel_config.get("service", {}) if isinstance(otel_config, dict) else {}
        pipelines = service.get("pipelines", {}) if isinstance(service, dict) else {}

        pipeline_info = {}
        for pipe_name, pipe_cfg in pipelines.items():
            if isinstance(pipe_cfg, dict):
                pipeline_info[pipe_name] = {
                    'receivers': pipe_cfg.get('receivers', []),
                    'processors': pipe_cfg.get('processors', []),
                    'exporters': pipe_cfg.get('exporters', []),
                }

        # Extract receiver/processor/exporter configs (names only)
        receivers = sorted(otel_config.get("receivers", {}).keys()) if isinstance(otel_config.get("receivers"), dict) else []
        processors = sorted(otel_config.get("processors", {}).keys()) if isinstance(otel_config.get("processors"), dict) else []
        exporters = sorted(otel_config.get("exporters", {}).keys()) if isinstance(otel_config.get("exporters"), dict) else []

        collectors.append({
            'name': meta.get("name", ""),
            'namespace': meta.get("namespace", ""),
            'mode': spec.get("mode", ""),
            'source_chart': doc.get("_source_chart", ""),
            'receivers': receivers,
            'processors': processors,
            'exporters': exporters,
            'pipelines': pipeline_info,
        })

    # Also parse Instrumentation resources
    instrumentations = []
    for doc in get_resources_by_kind(docs, "Instrumentation"):
        meta = doc.get("metadata", {})
        spec = doc.get("spec", {})
        instrumentations.append({
            'name': meta.get("name", ""),
            'namespace': meta.get("namespace", ""),
            'exporter_endpoint': spec.get("exporter", {}).get("endpoint", ""),
            'propagators': spec.get("propagators", []),
        })

    return {
        'total_collectors': len(collectors),
        'total_instrumentations': len(instrumentations),
        'collectors': collectors,
        'instrumentations': instrumentations,
    }


# ---------------------------------------------------------------------------
# Service Monitors (from rendered ServiceMonitor/PodMonitor)
# ---------------------------------------------------------------------------


def generate_service_monitors(docs: list[dict]) -> dict[str, Any]:
    """Generate service_monitors.yaml from rendered resources."""
    monitors = []

    for kind in ("ServiceMonitor", "PodMonitor"):
        for doc in get_resources_by_kind(docs, kind):
            meta = doc.get("metadata", {})
            spec = doc.get("spec", {})

            # Extract endpoints
            endpoints_key = "endpoints" if kind == "ServiceMonitor" else "podMetricsEndpoints"
            endpoints = spec.get(endpoints_key, [])

            endpoint_info = []
            for ep in endpoints:
                if isinstance(ep, dict):
                    endpoint_info.append({
                        'port': ep.get('port', ''),
                        'path': ep.get('path', '/metrics'),
                        'interval': ep.get('interval', ''),
                        'scheme': ep.get('scheme', ''),
                    })

            # Extract selector
            selector = spec.get("selector", {})
            match_labels = selector.get("matchLabels", {})

            # Namespace selector
            ns_selector = spec.get("namespaceSelector", {})

            monitors.append({
                'kind': kind,
                'name': meta.get("name", ""),
                'namespace': meta.get("namespace", ""),
                'source_chart': doc.get("_source_chart", ""),
                'selector_labels': match_labels,
                'namespace_selector': ns_selector,
                'endpoints': endpoint_info,
            })

    return {
        'total_monitors': len(monitors),
        'service_monitors': [m for m in monitors if m['kind'] == 'ServiceMonitor'],
        'pod_monitors': [m for m in monitors if m['kind'] == 'PodMonitor'],
    }


