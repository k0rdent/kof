def generate_kubernetes_resources(docs: list[dict]) -> dict[str, Any]:
    """Generate kubernetes_resources.yaml — expected resource inventory."""
    resources_by_key = {}

    for doc in docs:
        kind = doc.get("kind", "")
        if not kind:
            continue

        meta = doc.get("metadata", {})
        source_chart = doc.get("_source_chart", "")
        key = (kind, meta.get("namespace", ""), meta.get("name", ""))
        if key not in resources_by_key:
            resources_by_key[key] = {
                'kind': kind,
                'name': meta.get("name", ""),
                'namespace': meta.get("namespace", ""),
                'source_chart': source_chart,
                'source_charts': [source_chart] if source_chart else [],
            }
            continue

        source_charts = resources_by_key[key]['source_charts']
        if source_chart and source_chart not in source_charts:
            source_charts.append(source_chart)

    resources = sorted(
        resources_by_key.values(),
        key=lambda r: (r['kind'], r['namespace'], r['name']),
    )

    # Group by kind for summary
    by_kind = {}
    for r in resources:
        by_kind.setdefault(r['kind'], []).append(r)

    # Filter to observability-relevant kinds for detailed listing
    observability_resources = [r for r in resources if r['kind'] in OBSERVABILITY_KINDS]

    # Also scan for resources not rendered (from kof-storage templates)
    unrendered_resources = _scan_unrendered_templates()

    return {
        'total_resources': len(resources),
        'total_observability_resources': len(observability_resources),
        'by_kind_summary': {k: len(v) for k, v in sorted(by_kind.items())},
        'observability_resources': observability_resources,
        'unrendered_resources_note': (
            'Some resources require runtime context (Lookup, Secrets) '
            'and cannot be rendered with helm template. Listed below from source templates.'
        ),
        'unrendered_resources': unrendered_resources,
    }


def _scan_unrendered_templates() -> list[dict]:
    """Scan chart templates for resources that helm template cannot render."""
    unrendered = []

    # kof-storage templates
    storage_templates = CHARTS_DIR / "kof-storage" / "templates"
    if storage_templates.exists():
        for f in sorted(storage_templates.rglob("*.yaml")):
            text = f.read_text()
            for kind in OBSERVABILITY_KINDS:
                if f"kind: {kind}" in text:
                    name_match = re.search(r'name:\s*(.+)', text[text.find(f'kind: {kind}'):])
                    name = name_match.group(1).strip() if name_match else f.stem
                    name = re.sub(r'\{\{.*?\}\}', 'TMPL', name).strip('"').strip("'")
                    unrendered.append({
                        'kind': kind,
                        'name': name,
                        'source_template': str(f.relative_to(REPO_ROOT)),
                        'chart': 'kof-storage',
                    })

    return unrendered


