def generate_alerts_and_rules(docs: list[dict]) -> tuple[dict, dict]:
    """Generate alerts.yaml and recording_rules.yaml from rendered PrometheusRule."""
    prom_rules = get_resources_by_kind(docs, "PrometheusRule")

    all_alerts = []
    all_records = []
    alert_metrics = set()
    record_metrics = set()

    for pr in prom_rules:
        meta = pr.get("metadata", {})
        resource_name = meta.get("name", "")
        namespace = meta.get("namespace", "")
        source_chart = pr.get("_source_chart", "")

        spec = pr.get("spec", {})
        for group in spec.get("groups", []):
            group_name = group.get("name", "")
            for rule in group.get("rules", []):
                expr = rule.get("expr", "")
                if isinstance(expr, (int, float)):
                    expr = str(expr)
                # Strip PromQL comments (lines starting with #)
                expr_clean = '\n'.join(
                    l for l in expr.split('\n')
                    if not l.strip().startswith('#')
                )
                expr_clean = re.sub(r'\s+', ' ', expr_clean).strip()
                metrics = extract_metrics_from_expr(expr_clean)

                if "alert" in rule:
                    severity = rule.get("labels", {}).get("severity", "")
                    for_duration = rule.get("for", "")
                    all_alerts.append({
                        'name': rule["alert"],
                        'group': group_name,
                        'expr': expr_clean,
                        'for': for_duration,
                        'severity': severity,
                        'metrics': metrics,
                        'resource': resource_name,
                        'namespace': namespace,
                        'source_chart': source_chart,
                    })
                    alert_metrics.update(metrics)

                elif "record" in rule:
                    all_records.append({
                        'name': rule["record"],
                        'group': group_name,
                        'expr': expr_clean,
                        'metrics': metrics,
                        'resource': resource_name,
                        'namespace': namespace,
                        'source_chart': source_chart,
                    })
                    record_metrics.update(metrics)

    alerts_data = {
        'total_alerts': len(all_alerts),
        'total_groups': len(set(a['group'] for a in all_alerts)),
        'unique_referenced_metrics': sorted(alert_metrics),
        'alerts': all_alerts,
    }

    rules_data = {
        'total_recording_rules': len(all_records),
        'total_groups': len(set(r['group'] for r in all_records)),
        'unique_referenced_metrics': sorted(record_metrics),
        'recording_rules': all_records,
    }

    return alerts_data, rules_data


# ---------------------------------------------------------------------------
# Metrics (aggregated from all sources)
# ---------------------------------------------------------------------------


def generate_metrics(dashboard_metrics: set, alerts_data: dict,
                     rules_data: dict) -> dict[str, Any]:
    """Generate metrics.yaml with classification of all referenced metrics.

    Dashboard-referenced metrics are informational (required=false).
    Only critical_runtime_metrics are required=true.
    """
    alert_metrics = set(alerts_data.get('unique_referenced_metrics', []))
    record_metrics = set(rules_data.get('unique_referenced_metrics', []))
    critical_metrics = set(CRITICAL_RUNTIME_METRICS)

    all_metrics = dashboard_metrics | alert_metrics | record_metrics | critical_metrics

    # Classify each metric
    classified = []
    for m in sorted(all_metrics):
        entry = {
            'name': m,
            'sources': [],
            'required': m in critical_metrics,
        }
        if m in dashboard_metrics:
            entry['sources'].append('dashboard')
        if m in alert_metrics:
            entry['sources'].append('alert')
        if m in record_metrics:
            entry['sources'].append('recording_rule')
        if m in critical_metrics:
            entry['sources'].append('critical_runtime')
        classified.append(entry)

    # Required labels (common across KOF queries)
    required_labels = [
        {'name': 'cluster', 'description': 'Cluster name identifier'},
        {'name': 'namespace', 'description': 'Kubernetes namespace'},
        {'name': 'pod', 'description': 'Pod name'},
        {'name': 'container', 'description': 'Container name'},
        {'name': 'node', 'description': 'Node name'},
        {'name': 'job', 'description': 'Scrape job name'},
        {'name': 'service', 'description': 'Service name'},
        {'name': 'instance', 'description': 'Target instance'},
    ]

    return {
        'total_unique_metrics': len(all_metrics),
        'dashboard_referenced_count': len(dashboard_metrics),
        'alert_referenced_count': len(alert_metrics),
        'recording_rule_referenced_count': len(record_metrics),
        'critical_runtime_count': len(critical_metrics),
        'required_labels': required_labels,
        'critical_runtime_metrics': sorted(critical_metrics),
        'metrics': classified,
    }


