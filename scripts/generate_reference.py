# ---------------------------------------------------------------------------


def strip_go_templates(text: str) -> str:
    """Remove Go template expressions so YAML can be parsed."""
    text = re.sub(r'\{\{`', '', text)
    text = re.sub(r'`\}\}', '', text)
    text = re.sub(r'\{\{-?\s*/\*.*?\*/\s*-?\}\}', '', text, flags=re.DOTALL)
    text = re.sub(r'^[ \t]*\{\{-?.*?-?\}\}[ \t]*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'\{\{.*?\}\}', 'TMPL', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text


def _unquote_yaml_scalar(value: str) -> str:
    """Best-effort unquote for scalar strings found by the fallback parser."""
    value = value.strip()
    if len(value) >= 2 and value[0] == value[-1] and value[0] in {"'", '"'}:
        value = value[1:-1]
    return value.replace("''", "'")


def _extract_exprs_from_text(text: str) -> list[str]:
    """Fallback extraction for dashboards that are not valid YAML."""
    lines = text.splitlines()
    expressions = []
    expr_re = re.compile(r'^(?P<indent>\s*)expr:\s*(?P<value>.*)$')

    for idx, line in enumerate(lines):
        match = expr_re.match(line)
        if not match:
            continue

        raw_value = match.group('value').strip()
        if not raw_value:
            continue

        if raw_value in {'|', '|-', '|+', '>', '>-', '>+'}:
            base_indent = len(match.group('indent'))
            block_indent = None
            block_lines = []

            for next_line in lines[idx + 1:]:
                if not next_line.strip():
                    block_lines.append('')
                    continue

                indent = len(next_line) - len(next_line.lstrip(' '))
                if indent <= base_indent:
                    break

                if block_indent is None:
                    block_indent = indent
                block_lines.append(next_line[block_indent:])

            expr = ' '.join(part.strip() for part in block_lines if part.strip())
        else:
            expr = _unquote_yaml_scalar(raw_value)

        expr = re.sub(r'\s+', ' ', expr.strip())
        if expr and expr != 'TMPL':
            expressions.append(expr)

    return expressions


def parse_dashboard_file_fallback(filepath: Path, parse_error: Exception) -> dict[str, Any] | None:
    """Parse only dashboard expressions when the source is not valid YAML."""
    raw_text = filepath.read_text()
    text = strip_go_templates(raw_text)
    expressions = _extract_exprs_from_text(text)
    if not expressions:
        raise ValueError(f"failed to parse dashboard {filepath}: {parse_error}") from parse_error

    # Try to extract title via regex (top-level "title: ..." line)
    title = ''
    title_match = re.search(r'^title:\s*(.+)$', raw_text, re.MULTILINE)
    if title_match:
        title = title_match.group(1).strip().strip('"').strip("'")

    extracted_panels = []
    all_metrics = set()
    for expr in expressions:
        metrics = extract_metrics_from_expr(expr)
        all_metrics.update(metrics)
        extracted_panels.append({
            'title': '',
            'type': '',
            'queries': [{
                'expr': expr,
                'datasource_type': '',
                'metrics': metrics,
            }],
        })

    return {
        'name': filepath.stem,
        'title': title,
        'category': filepath.parent.name,
        'file': str(filepath.relative_to(REPO_ROOT)),
        'panel_count': len(extracted_panels),
        'referenced_metrics': sorted(all_metrics),
        'panels': extracted_panels,
        'parse_mode': 'regex_fallback',
        'parse_error': str(parse_error).split('\n', 1)[0],
    }


def parse_dashboard_file(filepath: Path) -> dict[str, Any] | None:
    """Parse a dashboard YAML file and extract panel info."""
    text = filepath.read_text()
    cleaned = strip_go_templates(text)
    try:
        data = yaml.safe_load(cleaned)
    except yaml.YAMLError as e:
        return parse_dashboard_file_fallback(filepath, e)

    if not data or not isinstance(data, dict):
        return None

    panels = data.get('panels', [])
    if not panels:
        return None

    category = filepath.parent.name
    dashboard_name = filepath.stem

    extracted_panels = []
    for panel in panels:
        if not isinstance(panel, dict):
            continue
        panel_info = _extract_panel(panel)
        if panel_info:
            extracted_panels.append(panel_info)
        for sub in panel.get('panels', []):
            if isinstance(sub, dict):
                sub_info = _extract_panel(sub)
                if sub_info:
                    extracted_panels.append(sub_info)

    if not extracted_panels:
        return None

    all_metrics = set()
    for p in extracted_panels:
        for q in p.get('queries', []):
            all_metrics.update(q.get('metrics', []))

    return {
        'name': dashboard_name,
        'title': data.get('title', ''),
        'category': category,
        'file': str(filepath.relative_to(REPO_ROOT)),
        'panel_count': len(extracted_panels),
        'referenced_metrics': sorted(all_metrics),
        'panels': extracted_panels,
        'parse_mode': 'yaml',
    }


def _extract_panel(panel: dict) -> dict[str, Any] | None:
    """Extract query info from a single panel."""
    title = panel.get('title', '')
    panel_type = panel.get('type', '')
    targets = panel.get('targets', [])
    if not targets:
        return None

    queries = []
    for target in targets:
        if not isinstance(target, dict):
            continue
        expr = target.get('expr', '')
        if not expr or expr == 'TMPL':
            continue
        expr = re.sub(r'\s+', ' ', expr.strip())
        ds = target.get('datasource', {})
        ds_type = ds.get('type', '') if isinstance(ds, dict) else ''
        metrics = extract_metrics_from_expr(expr)
        queries.append({
            'expr': expr,
            'datasource_type': ds_type,
            'metrics': metrics,
        })

    if not queries:
        return None

    return {'title': title, 'type': panel_type, 'queries': queries}


def generate_dashboards(docs: list[dict]) -> tuple[dict[str, Any], dict[str, Any], set[str]]:
    """Generate dashboards.yaml (slim) and dashboard_queries.full.yaml (detailed).

    Returns (slim_data, full_data, all_metrics) tuple.
    """
    parsed = []
    all_metrics = set()

    # Parse dashboard source files for panel/query info
    if DASHBOARDS_DIR.exists():
        for yaml_file in sorted(DASHBOARDS_DIR.rglob('*.yaml')):
            result = parse_dashboard_file(yaml_file)
            if result:
                parsed.append(result)
                all_metrics.update(result['referenced_metrics'])

    # Cross-reference with rendered GrafanaDashboard resources
    rendered_names = set()
    for doc in get_resources_by_kind(docs, "GrafanaDashboard"):
        meta = doc.get("metadata", {})
        rendered_names.add(meta.get("name", ""))

    source_names = {d['name'] for d in parsed}

    # Classify dashboards
    source_only = sorted(source_names - rendered_names)
    rendered_only = sorted(rendered_names - source_names)
    matched = sorted(source_names & rendered_names)

    # Build slim dashboard entries
    slim_dashboards = []
    for d in parsed:
        slim_dashboards.append({
            'name': d['name'],
            'title': d.get('title') or d['name'],
            'category': d['category'],
            'file': d['file'],
            'rendered': d['name'] in rendered_names,
        })

    slim_data = {
        'total_dashboards': len(parsed),
        'total_rendered_grafanadashboard_resources': len(rendered_names),
        'source_only': source_only,
        'rendered_only': rendered_only,
        'matched': matched,
        'dashboards': slim_dashboards,
    }

    # Build full query detail file
    full_entries = []
    for d in parsed:
        for p in d['panels']:
            for q in p.get('queries', []):
                full_entries.append({
                    'dashboard': d['name'],
                    'category': d['category'],
                    'panel_title': p['title'],
                    'panel_type': p['type'],
                    'expr': q['expr'],
                    'datasource_type': q.get('datasource_type', ''),
                    'metrics': q.get('metrics', []),
                })

    full_data = {
        'total_queries': len(full_entries),
        'queries': full_entries,
    }

    return slim_data, full_data, all_metrics


