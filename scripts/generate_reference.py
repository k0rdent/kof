def extract_metrics_from_expr(expr: str) -> list[str]:
    """Extract metric names from a PromQL expression."""
    if not expr:
        return []
    # Strategy: match identifiers followed by { or ( or end-of-context
    # that appear OUTSIDE of label matchers (inside {})
    # First, remove everything inside {} to avoid matching label names/values
    no_braces = re.sub(r'\{[^}]*\}', '{}', expr)
    # Remove string literals
    no_braces = re.sub(r'"[^"]*"', '""', no_braces)
    no_braces = re.sub(r"'[^']*'", "''", no_braces)
    # Remove backtick literals
    no_braces = re.sub(r'`[^`]*`', '``', no_braces)

    # Match metric names: identifiers followed by { or ( or whitespace/operator
    metrics = re.findall(r'([a-zA-Z_:][a-zA-Z0-9_:]*)\s*(?:\{|\(|$|[\s,)\]])', no_braces)

    # Filter PromQL functions/keywords
    promql_keywords = {
        'sum', 'avg', 'min', 'max', 'count', 'stddev', 'stdvar',
        'topk', 'bottomk', 'quantile', 'count_values',
        'rate', 'irate', 'increase', 'delta', 'idelta',
        'histogram_quantile', 'histogram_avg', 'histogram_count',
        'label_replace', 'label_join',
        'abs', 'absent', 'absent_over_time', 'ceil', 'floor', 'round',
        'changes', 'clamp', 'clamp_max', 'clamp_min',
        'deriv', 'exp', 'ln', 'log2', 'log10', 'sqrt',
        'predict_linear', 'resets', 'sort', 'sort_desc',
        'time', 'timestamp', 'vector', 'scalar',
        'on', 'ignoring', 'group_left', 'group_right',
        'by', 'without', 'unless', 'and', 'or', 'offset',
        'bool', 'inf', 'nan',
        'avg_over_time', 'min_over_time', 'max_over_time',
        'sum_over_time', 'count_over_time', 'stddev_over_time',
        'last_over_time', 'present_over_time', 'quantile_over_time',
        'sgn', 'deg', 'rad', 'acos', 'asin', 'atan', 'cos', 'sin', 'tan',
        'days_in_month', 'day_of_month', 'day_of_week', 'day_of_year',
        'hour', 'minute', 'month', 'year', 'group', 'pi',
        'TMPL',  # template placeholder
    }
    # Common label names/values that are NOT metrics
    not_metrics = {
        'cpu', 'memory', 'job', 'node', 'pod', 'container', 'namespace',
        'service', 'instance', 'name', 'type', 'mode', 'key', 'url',
        'cluster', 'severity', 'alertname', 'alertstate', 'endpoint',
        'resource', 'device', 'mountpoint', 'fstype', 'le',
        'quantile', 'reason', 'condition', 'status', 'phase',
        'TMPL', 'true', 'false', 'null',
        # Grafana variables
        '__interval', '__rate_interval', '__auto',
        # LogQL / VictoriaLogs fields
        '_msg', '_time', '_stream',
    }
    return sorted(set(
        m for m in metrics
        if m.lower() not in promql_keywords
        and m not in not_metrics
        and not m.startswith('$')
        and not m.startswith('__')
        and (len(m) > 3 or m == 'up')  # skip 1-3 char tokens (label values)
    ))


